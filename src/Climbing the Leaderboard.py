import bisect

def climbingLeaderboard(ranked, player):
      new_ranked = sorted(list(set(ranked)))
      player = sorted(player)
      n = len(new_ranked)+1
      final_list = []
      for value in player:
           final_list.append(n - bisect.bisect(new_ranked, value))
      return final_list
      
